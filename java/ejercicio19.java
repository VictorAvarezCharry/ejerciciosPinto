public class ejercicio19 {
    public static void main(String[] args) {
        
        int K;  
        int N;  
        
        
        N = 2;
        System.out.println("Valor inicial: " + N);
        
        
        for (K = 1; K <= 4; K++) {
            N = N + 2;
            System.out.println("Valor " + K + ": " + N);
        }
    }
}